#include "../include/ft.h"

int		main(int ac, char **av)
{
	int a;

	if (ac < 2)
	{
        return (1);
    }

	a = 1;
    while (a < ac)
    {
        ft_error(av[a], ft_cat(av[a]));
        a++;
    }

    return (0);
}
